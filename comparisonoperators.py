'''
COMPARISON OPERATORS

Used to compare informations.
It returns a True or False answer, based on the information given.

    ==   equal
    !=   not equal
    >    greater than
    <    less than
    >=   greater than or equal to
    <=   less than or equal to
'''

# == equal

operators = 10 == 12
print(operators)

# != not equal

operators = 10 != 12
print(operators)

# > greater than

operators = 10 > 12
print(operadores)

# < less than

operators = 10 < 12
print(operators)

# >= greater than or equal to

operators = 10 >= 12
print(operators)

# <= less than or equal to

operators = 10 <= 12
print(operators)