'''
STR, INT, FLOAT

    str: is understood by the program as text, so the code was executed on the console as 33.
    int: is understood by the program as a number, so the result will comeback as 8.
    float: is understood by the program as a fraction, so the result will always comeback as a fraction calculation.
'''

x = str(3)
y = int(4)
z = float (5)

print(x+x)
print(y+y)
print(z+z)